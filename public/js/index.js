// When Extention DOM Loads
document.addEventListener('DOMContentLoaded', function() {
    Store.init()
    let notes = document.querySelectorAll(".note");
    notes.forEach((note) => {
        note.addEventListener("input", Store.saveNotes)
    })
    let quote = document.querySelector('.quote');
    let author = document.querySelector('.author');
    let randomQuote = islamic[Math.floor(Math.random() * islamic.length)];
    quote.innerText = randomQuote;
    author.innerText = "Hazrat Ali RA";
});


class Store {
    static getNotes = () => {
        let notes = JSON.parse(localStorage.getItem("notes")) ?? []
        return notes
    }

    static saveNotes = () => {
        let notes = document.querySelectorAll(".note");
        let data = []
        notes.forEach((note, index) => {
            data.push({ id: note.id, note: note.value })
        })
        localStorage.setItem('notes', JSON.stringify(data))
    }

    static init = () => {
        let notes = this.getNotes();
        notes.forEach((note, index) => {
            document.querySelector("#"+note.id).value = note.note
        })
    }
}